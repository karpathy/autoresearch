# Low-precision dtype: bf16 on CUDA, fp16 on MPS, fp32 on CPU
_LOW_DTYPE = (
    torch.bfloat16 if PLATFORM.supports_bf16
    else torch.float16 if PLATFORM.supports_fp16
    else torch.float32
)

